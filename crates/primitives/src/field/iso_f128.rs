//! The explicit field isomorphism between the two GF(2^128) representations:
//! GHASH [`super::gf2_128::F128`] and the tower [`super::tower_f128::F128T`].
//!
//! Constructed once offline (scratchpad/iso.py): X below is a root of
//! x^64+x^4+x^3+x+1 inside GHASH's 2^64-subfield (found by Cantor-Zassenhaus
//! trace splitting), Y solves y^2 + y = X^61 (an F_2-linear solve), and
//! psi(x^i y^j) = X^i Y^j extends F_2-linearly to a field isomorphism; phi is
//! its matrix inverse. The tests re-verify multiplicativity at runtime.
//!
//! Used at the flock boundary of the 64-bit transition: flock's zerocheck
//! (phi_8, GF(2^8) NTT, univariate skip) stays entirely in GHASH; its output
//! claim (point and value) maps through [`ghash_to_tower`] before the
//! ring-switch reduction and the K-committed PCS, which live in the tower.
//! Mapping a claim is sound because the packed polynomial has F_2 (bit)
//! coefficients: for a bit-valued multilinear w, w-hat(phi(r)) = phi(w-hat(r)).

use super::gf2_128::F128;
use super::tower_f128::F128T;

/// phi(a): GHASH to tower.
pub fn ghash_to_tower(a: F128) -> F128T {
    let mut c0 = 0u64;
    let mut c1 = 0u64;
    for (k, &(t0, t1)) in GHASH_TO_TOWER.iter().enumerate() {
        let bit = if k < 64 { (a.lo >> k) & 1 } else { (a.hi >> (k - 64)) & 1 };
        let m = 0u64.wrapping_sub(bit);
        c0 ^= t0 & m;
        c1 ^= t1 & m;
    }
    F128T::new(c0, c1)
}

/// psi(a): tower to GHASH.
pub fn tower_to_ghash(a: F128T) -> F128 {
    let mut lo = 0u64;
    let mut hi = 0u64;
    for (k, &(g0, g1)) in TOWER_TO_GHASH.iter().enumerate() {
        let bit = if k < 64 { (a.c0 >> k) & 1 } else { (a.c1 >> (k - 64)) & 1 };
        let m = 0u64.wrapping_sub(bit);
        lo ^= g0 & m;
        hi ^= g1 & m;
    }
    F128::new(lo, hi)
}

