#[cfg(test)]
mod tests {
    use super::*;

    fn splitmix64(state: &mut u64) -> u64 {
        *state = state.wrapping_add(0x9E37_79B9_7F4A_7C15);
        let mut z = *state;
        z = (z ^ (z >> 30)).wrapping_mul(0xBF58_476D_1CE4_E5B9);
        z = (z ^ (z >> 27)).wrapping_mul(0x94D0_49BB_1331_11EB);
        z ^ (z >> 31)
    }

    #[test]
    fn is_a_field_isomorphism() {
        assert_eq!(ghash_to_tower(F128::ONE), F128T::ONE);
        assert_eq!(tower_to_ghash(F128T::ONE), F128::ONE);
        assert_eq!(ghash_to_tower(F128::ZERO), F128T::ZERO);
        let mut s = 1u64;
        for _ in 0..2_000 {
            let a = F128::new(splitmix64(&mut s), splitmix64(&mut s));
            let b = F128::new(splitmix64(&mut s), splitmix64(&mut s));
            // multiplicative, additive, and a two-sided roundtrip
            assert_eq!(ghash_to_tower(a * b), ghash_to_tower(a) * ghash_to_tower(b));
            assert_eq!(ghash_to_tower(a + b), ghash_to_tower(a) + ghash_to_tower(b));
            assert_eq!(tower_to_ghash(ghash_to_tower(a)), a);
        }
        let mut s = 2u64;
        for _ in 0..2_000 {
            let t = F128T::new(splitmix64(&mut s), splitmix64(&mut s));
            assert_eq!(ghash_to_tower(tower_to_ghash(t)), t);
        }
    }
}
